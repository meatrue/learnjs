// Каким будет результат этих выражений?

5 > 4                    // true
"ананас" > "яблоко"      // false
"2" > "12"               // true - т.к. сравниваются 2 строки
undefined == null        // true
undefined === null       // false
null == "\n0\n"          // false - т.к. null равен только себе и undefined
null === +"\n0\n"        // false - т.к. точное равенство без приведения типов
